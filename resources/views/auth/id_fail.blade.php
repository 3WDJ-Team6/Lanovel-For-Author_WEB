<p class="text_p" tabindex="-1">目（め）を開（あ）けると、白（しろ）くて肉（にく）つきのできたお尻（しり）が目（め）の前（まえ）にあった。</p>
<p class="text_p" tabindex="-1"><b>「......ひかり、またお前（まえ）か」</b></p>
<p class="text_p" tabindex="-1"><span class="tem_effect"><span id="starlight" class="css_eft"
            style="width: 400px; height: 224.906px;"></span><img id="obj_30"
            src="https://s3.ap-northeast-2.amazonaws.com/lanovebucket/Author/authorID@google.com/images/1563272385hikari.PNG"
            alt="alt" class="resize" /></span></p>
<p class="text_p" tabindex="-1">名前（なまえ）を呼（よ）ぶと彼女（かのじょ）を抱（いだ）くように寄（よ）り添（そ）い、愛嬌を振（ふ）り回（まわ）す音（おと）ロールの耳（みみ）をくすぐった.</p>
<p class="text_p" tabindex="-1">それにしてもカンダ·ソラタは、鼻（はな）の先（さき）にあるヒカリのお尻（しり）を押（お）しのけ、灰色（はいろ）の絨毯（じゅうたん）で身（からだ）を起（お）こした。</p>
<p class="text_p" tabindex="-1"></p>
<p class="text_p" tabindex="-1"><span
        style="font-weight: bolder;">「切（せつ）ないね......」</span>薄目（うすめ）をあけてまぶしい窓（まど）の外（そと）を眺（なが）めた.西（にし）の空（そら）が、この世（よ）の果（は）てを告（つ）げるように真（ま）っ赤（か）に燃（も）えあがっていた。
</p>
<p class="text_p" tabindex="-1">無理（むり）やり押（お）し流（なが）された光（ひかり）が倒（たお）れたように泣（な）いたが、空太（そらた）はため息（いき）をついただけだった。<br /></p>
<p class="text_p" tabindex="-1">「生れてると猫（ねこ）のお尻（しり）···私（わたし）の青春（せいしゅん）も本当（ほんとう）に切（せつ）ない。」</p>
<p class="text_p" tabindex="-1">迫（せま）ってくる虚脱感（きょだつかん）に空振り（からぶり）する手（て）で顔（かお）を覆（くつがえ）った。</p>
<p class="text_p" tabindex="-1">「いいえ、青春物語（せいしゅんものがたり）やってるのがもっと切ないか。」</p>
<p class="text_p" tabindex="-1">
    だからと言って、同意（どうい）でもするように、膝に乗（の）せた白（しろ）い猫（ねこ）のヒカリがあくびをした。そして、4坪程度の部屋（へや）にいた他（ほか）の六匹（ろっぴき）の猫（ねこ）たちがご飯（はん）を大領（たいりょう）するというように一斉（いっさい）に合唱を開始（かいし）した。
</p>
<p class="text_p" tabindex="-1">
    白色（はくしょく）、黒色（こくしょく）、三色（さんしょく）、茶色（ちゃいろ）テビ、暗褐色（あんかっしょく）テベ、シャム似（に）たようなやつ、アメリカン・ショートヘアのような初々（ういうい）しい子供（こども）たちがバラエティーするように整（ととの）った七匹（ななひき）の子猫（こねこ）はすべて空太（そらた）が拾（ひろ）ってきたギルニャンイだ。
</p>
<p class="text_p" tabindex="-1">几帳面（きちょうめん）に光（ひかり）、のぞみ、児玉（こだま）、翼（つばさ）、小町（こまち）、青葉（あおば）、旭（あさひ）と名付（なつ）けてくれた。</p>
<p class="text_p" tabindex="-1">ご飯（はん）をくれという猫（ねこ）に、ソラタはグーという声（こえ）で答（こた）えた。主人（しゅじん）たちも飢（か）えを我慢（がまん）していると訴（うった）えるのだ。</p>
<p class="text_p" tabindex="-1">春休み（はるやすみ）の最終日（さいしゅうび）の4月5日。夕方五時（ゆうがたごじ）。</p>
<p class="text_p" tabindex="-1">
    吹（ふ）くことに染（そ）まった2階建て（にかいたて）の古（ふる）い木造（もくぞう）アパートは水明芸術大学付属高等学校（すいめいげいじゅつだいがくふぞくこうとうがっこう）の学生寮（がくせいりょう）だ。</p>
<p class="text_p" tabindex="-1">庭（にわ）にある大（おお）きな桜（さくら）の木（き）のためか、名前（なまえ）は桜山（さくらやま）という。</p>
<p class="text_p" tabindex="-1">台所（だいどころ）と食堂（しょくどう）、浴室（よくしつ）は共用（きょうよう）。</p>
<p class="text_p" tabindex="-1">学校（がっこう）まで徒歩（とほ）10分。一番（いちばん）近（ちか）い駅（えき）まで徒歩（とほ）10分。</p>
<p class="text_p" tabindex="-1">この桜長（さくらちょう）の101号が今春（こんしゅん）から2年生（にねんせい）になる神田空太（かんだそらた）の本拠地（ほんきょち）であった。</p>
<p class="text_p" tabindex="-1">部屋（へや）の壁（かべ）には「目標（もくひょう）!サクラ場脱出!!「と新年（しんねん）が明けるやいなや新春揮毫で書いた計画（けいかく）が堂々たる筆致で踊（おど）っていた。
</p>
<p class="text_p" tabindex="-1">
    ソラタの当面（とうめん）の目標（もくひょう）はガールフレンドを作（つく）るのも、高校野球（こうこうやきゅう）の全国大会（ぜんこくたいかい）である甲子園（こうしえん）に出（で）るわけでもない。もちろん、国立競技場（こくりつきょうぎじょう）も全国高校総合体育大会（ぜんこくそうこういおうだいいくたいかい）でもない。この寮（りょう）を脱出（だっしゅつ）するのだ。
</p>
<p class="text_p" tabindex="-1">理由（りゆう）の「即ちソン「、この桜場（さくらば）は他（ほか）の寮（りょう）とは少（すこ）し違（ちが）うからだ。</p>
<p class="text_p" tabindex="-1">
    一般寮（いっぱんりょう）の共同生活（きょうどうせいかつ）に適応（てきおう）できなかった生徒（せいと）たちが集（あつ）まっている更生（こうせい）の場（ば）であり、はっきりと言（い）うので問題児（もんだいじ）たちの巣窟（そうくつ）だ。
</p>
<p class="text_p" tabindex="-1">
    一般の寮と違い、舎監先生も食堂（しょくどう）もないため、炊事（すいじ）、洗濯（せんたく）、掃除（そうじ）のような家事全般（かじぜんぱん）を自分（じぶん）でやらなければならないことは、全く（まったく）煩（わずら）しいことだ。</p>
<p class="text_p" tabindex="-1">学校側（がっこうがわ）では自立心（じりつしん）を育（そだ）てるためだというが、ソラタは実（じつ）は人（ひと）を探（さが）せないからではないかと思（おも）っていた。</p>
<p class="text_p" tabindex="-1">とにかく桜井市（さくらいし）は名前（なまえ）を取（と）り出（だ）すだけでも友人（ゆうじん）を逃（に）げるほどの破壊力（はかいりょく）を誇（さそ）った。</p>
<p class="text_p" tabindex="-1">もっと厄介（やっかい）なのは一ヶ月に一度、郊外（こうがい）の掃除（そうじ）に強制的（きょうせいてき）に参加（さんか）しなければならないということだ。</p>
<p class="text_p" tabindex="-1">
    その名（な）の通（とお）り学校周辺（がっこうしゅうへん）のゴミを拾（ひろ）うボランティア活動（かつどう）ではあるが、一周（いっしゅう）回（まわ）るのに成人（せいじん）の足（あし）で30分はかかる大学敷地（だいがくしきち）を基準（きじゅん）にした『周辺（しゅうへん）』であるため、一日（いちにち）をきちんと使（つか）わなければならない大（おお）きな行事（ぎょうじ）だった。
</p>
<p class="text_p" tabindex="-1">毎回（まいかい）、郊外掃除（こうないそうじ）の翌日（ゆくじつ）は、脚（あし）に筋肉痛（きんいくつう）ができるほどだった。</p>
<p class="text_p" tabindex="-1">
    このような不名誉（ふめいよ）な寮（りょう）に今（いま）は男女計四人（だんじょけいよにん）の学生（がくせい）と監視要員（かんしよういん）の教師（きょうし）1人が一緒（いっしょ）に暮（く）らしている。</p>

